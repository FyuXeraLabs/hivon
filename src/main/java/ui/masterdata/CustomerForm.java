    private void tblCustomersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomersMouseClicked
        int selectedRow = tblCustomers.getSelectedRow();
        if (selectedRow != -1) {
            String code = (String) tblCustomers.getValueAt(selectedRow, 0);
            selectedCustomer = findCustomerByCode(code);
            if (selectedCustomer != null) {
                txtCode.setText(selectedCustomer.getCustomerCode());
                txtName.setText(selectedCustomer.getCustomerName());
                txtContact.setText(selectedCustomer.getContactPerson());
                txtPhone.setText(selectedCustomer.getPhone());
                txtEmail.setText(selectedCustomer.getEmail());
                txtAddress.setText(selectedCustomer.getAddress());
                chkIsActive.setSelected(selectedCustomer.getIsActive() != null && selectedCustomer.getIsActive());
                txtCode.setEditable(false);
                isAddMode = false;
                updateButtonStates();
            }
        }
    }//GEN-LAST:event_tblCustomersMouseClicked

