    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (!validateForm() || selectedCustomer == null) {
            StatusMessageHandler.showWarning(txtStatus, "Please fill all required fields!");
            return;
        }

        selectedCustomer.setCustomerCode(txtCode.getText().trim());
        selectedCustomer.setCustomerName(txtName.getText().trim());
        selectedCustomer.setContactPerson(txtContact.getText().trim());
        selectedCustomer.setPhone(txtPhone.getText().trim());
        selectedCustomer.setEmail(txtEmail.getText().trim());
        selectedCustomer.setAddress(txtAddress.getText().trim());
        selectedCustomer.setIsActive(chkIsActive.isSelected());

        BackgroundTask task = new BackgroundTask(this, "Updating Customer") {

            @Override
            protected Boolean performTask() throws Exception {
                updateProgress("Updating customer...");
                return controller.updateCustomer(selectedCustomer);
            }

            @Override
            protected void onSuccess() {
                StatusMessageHandler.showSuccess(txtStatus, "Customer updated successfully!");
                clearForm();
                loadCustomerList();
                updateButtonStates();
            }

            @Override
            protected void onFailure(Exception e) {
                StatusMessageHandler.showError(txtStatus, "Failed to update customer: " + e.getMessage());
            }
        };
        task.executeWithDialog();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedCustomer == null) return;

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete customer " + selectedCustomer.getCustomerCode() + "?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        BackgroundTask task = new BackgroundTask(this, "Deleting Customer") {

            @Override
            protected Boolean performTask() throws Exception {
                updateProgress("Deleting customer...");
                return controller.deleteCustomer(selectedCustomer.getCustomerId());
            }

            @Override
            protected void onSuccess() {
                StatusMessageHandler.showSuccess(txtStatus, "Customer deleted successfully!");
                clearForm();
                loadCustomerList();
                updateButtonStates();
            }

            @Override
            protected void onFailure(Exception e) {
                StatusMessageHandler.showError(txtStatus, "Failed to delete customer: " + e.getMessage());
            }
        };
        task.executeWithDialog();
    }//GEN-LAST:event_btnDeleteActionPerformed

