    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!validateForm()) {
            StatusMessageHandler.showWarning(txtStatus, "Please fill all required fields!");
            return;
        }

        CustomerDTO dto = new CustomerDTO();
        dto.setCustomerCode(txtCode.getText().trim());
        dto.setCustomerName(txtName.getText().trim());
        dto.setContactPerson(txtContact.getText().trim());
        dto.setPhone(txtPhone.getText().trim());
        dto.setEmail(txtEmail.getText().trim());
        dto.setAddress(txtAddress.getText().trim());
        dto.setIsActive(chkIsActive.isSelected());

        BackgroundTask task = new BackgroundTask(this, "Saving Customer") {
            private int customerId;

            @Override
            protected Boolean performTask() throws Exception {
                updateProgress("Creating customer...");
                customerId = controller.createCustomer(dto);
                return customerId > 0;
            }

            @Override
            protected void onSuccess() {
                StatusMessageHandler.showSuccess(txtStatus, "Customer created successfully!");
                clearForm();
                loadCustomerList();
                updateButtonStates();
            }

            @Override
            protected void onFailure(Exception e) {
                StatusMessageHandler.showError(txtStatus, "Failed to create customer: " + e.getMessage());
            }
        };
        task.executeWithDialog();
    }//GEN-LAST:event_btnSaveActionPerformed

