    private void populateZoneTable(List<ZoneDTO> zones) {
        this.allZones = zones;
        DefaultTableModel model = (DefaultTableModel) tblZones.getModel();
        model.setRowCount(0);

        if (zones != null) {
            for (ZoneDTO z : zones) {
                model.addRow(new Object[]{
                    z.getZoneCode(),
                    z.getZoneName(),
                    z.getZoneType(),
                    z.getTotalBins(),
                    z.getTotalCapacity(),
                    String.format("%.1f%%", z.getUtilizationPercent())
                });
            }
        }
    }

    private ZoneDTO findZoneByCode(String code) {
        if (code == null || allZones == null) return null;
        for (ZoneDTO z : allZones) {
            if (z.getZoneCode().equals(code)) return z;
        }
        return null;
    }

