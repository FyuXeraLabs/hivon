    // loads and populates UOM data into the table

    private void loadUOMList() {
        BackgroundTask task = new BackgroundTask(this, "Loading UOMs") {
            private List<UOMDTO> uoms;

            @Override
            protected Boolean performTask() throws Exception {
                updateProgress("Fetching UOMs...");
                uoms = controller.getAllUOMs();
                return uoms != null;
            }

            @Override
            protected void onSuccess() {
                populateUOMTable(uoms);
            }

            @Override
            protected void onFailure(Exception e) {
                StatusMessageHandler.showError(txtStatus, "Failed to load UOMs!");
            }
        };
        task.executeWithDialog();
    }

    private void populateUOMTable(List<UOMDTO> uoms) {
        this.allUOMs = uoms;
        DefaultTableModel model = (DefaultTableModel) tblUOMs.getModel();
        model.setRowCount(0);

        if (uoms != null) {
            for (UOMDTO u : uoms) {
                model.addRow(new Object[]{
                    u.getParamKey(),
                    u.getParamValue(),
                    u.isActive() ? "Yes" : "No"
                });
            }
        }
    }

    private UOMDTO findUOMByCode(String code) {
        if (code == null || allUOMs == null) return null;
        for (UOMDTO u : allUOMs) {
            if (u.getParamKey().equals(code)) return u;
        }
        return null;
    }

    private boolean validateForm() {
        return !txtCode.getText().trim().isEmpty() && !txtName.getText().trim().isEmpty();
    }

    private void clearForm() {
        txtCode.setText("");
        txtName.setText("");
        chkIsActive.setSelected(false);
        txtCode.setEditable(true);
        selectedUOM = null;
        tblUOMs.clearSelection();
        isAddMode = false;
    }

    private void updateButtonStates() {
        btnAdd.setEnabled(false);
        btnSave.setEnabled(false);
        btnClear.setEnabled(selectedUOM != null);
        btnUpdate.setEnabled(selectedUOM != null);
        btnDelete.setEnabled(false);
    }

    private void setupTttFieldNavigation() {
        javax.swing.JTextField[] textFields = {txtSearch, txtCode, txtName};

        for (int i = 0; i < textFields.length; i++) {
            final int currentIndex = i;
            textFields[i].addKeyListener(new java.awt.event.KeyAdapter() {
                @Override
                public void keyPressed(java.awt.event.KeyEvent evt) {
                    if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_DOWN) {
                        evt.consume();
                        if (currentIndex < textFields.length - 1) {
                            textFields[currentIndex + 1].requestFocus();
                        }
                    } else if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_UP) {
                        evt.consume();
                        if (currentIndex > 0) {
                            textFields[currentIndex - 1].requestFocus();
                        }
                    }
                }
            });
        }
    }

