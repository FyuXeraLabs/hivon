    private void tblUOMsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUOMsMouseClicked
        int selectedRow = tblUOMs.getSelectedRow();
        if (selectedRow != -1) {
            String code = (String) tblUOMs.getValueAt(selectedRow, 0);
            selectedUOM = findUOMByCode(code);
            if (selectedUOM != null) {
                txtCode.setText(selectedUOM.getParamKey());
                txtName.setText(selectedUOM.getParamValue());
                chkIsActive.setSelected(selectedUOM.isActive());
                txtCode.setEditable(false);
                isAddMode = false;
                updateButtonStates();
            }
        }
    }//GEN-LAST:event_tblUOMsMouseClicked

