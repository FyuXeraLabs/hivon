    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!validateForm()) {
            StatusMessageHandler.showWarning(txtStatus, "Please fill all required fields!");
            return;
        }

        UOMDTO dto = new UOMDTO();
        dto.setParamKey(txtCode.getText().trim());
        dto.setParamValue(txtName.getText().trim());
        dto.setDescription(txtName.getText().trim());
        dto.setIsActive(chkIsActive.isSelected());

        BackgroundTask task = new BackgroundTask(this, "Saving UOM") {

            @Override
            protected Boolean performTask() throws Exception {
                updateProgress("Creating UOM...");
                return controller.createUOM(dto);
            }

            @Override
            protected void onSuccess() {
                StatusMessageHandler.showSuccess(txtStatus, "UOM created successfully!");
                clearForm();
                loadUOMList();
                updateButtonStates();
            }

            @Override
            protected void onFailure(Exception e) {
                StatusMessageHandler.showError(txtStatus, "Failed to create UOM: " + e.getMessage());
            }
        };
        task.executeWithDialog();
    }//GEN-LAST:event_btnSaveActionPerformed

