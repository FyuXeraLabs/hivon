    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        String password = new String(txtPassword.getPassword());
        String confirm = new String(txtConfirmPassword.getPassword());

        if (password.isEmpty() || confirm.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Both fields are required.", "Validation", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!password.equals(confirm)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match.", "Validation", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // validate complexity
        core.security.UserAuthentication auth = new core.security.UserAuthentication();
        if (!auth.validatePasswordComplexity(password)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Password must be at least 8 characters and contain:\n- Uppercase letter\n- Lowercase letter\n- Number\n- Special character", "Validation", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        result = password;
        dispose();
    }//GEN-LAST:event_btnOKActionPerformed

