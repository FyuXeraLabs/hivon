    private void tglbtnShowConfirmPwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tglbtnShowConfirmPwdActionPerformed
        // TODO add your handling code here:
        if (tglbtnShowConfirmPwd.isSelected()) {
            // show pwd
            txtConfirmPassword.setEchoChar((char) 0);
            tglbtnShowConfirmPwd.setIcon(new javax.swing.ImageIcon(getClass().getResource("/btnicn/hide-14.png")));
        } else {
            // hide pwd
            txtConfirmPassword.setEchoChar(defaultEchoChar);
            tglbtnShowConfirmPwd.setIcon(new javax.swing.ImageIcon(getClass().getResource("/btnicn/view-14.png")));
        }
    }//GEN-LAST:event_tglbtnShowConfirmPwdActionPerformed

