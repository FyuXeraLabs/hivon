    private void setupFieldNavigation() {
        
        javax.swing.JPasswordField[] passwordFields = {txtPassword, txtConfirmPassword};

        for (int i = 0; i < passwordFields.length; i++) {
            final int currentIndex = i;
            passwordFields[i].addKeyListener(new java.awt.event.KeyAdapter() {
                @Override
                public void keyPressed(java.awt.event.KeyEvent evt) {
                    if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_DOWN) {
                        evt.consume();
                        if (currentIndex < passwordFields.length - 1) {
                            passwordFields[currentIndex + 1].requestFocus();
                        }
                    } else if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_UP) {
                        evt.consume();
                        if (currentIndex > 0) {
                            passwordFields[currentIndex - 1].requestFocus();
                        }
                    }
                }
            });
        }
    }

